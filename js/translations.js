// Function to set the language
function setLanguage(lang) {
  // Update active button
  document.getElementById("en-toggle").classList.remove("active")
  document.getElementById("es-toggle").classList.remove("active")
  document.getElementById(`${lang}-toggle`).classList.add("active")

  // Update all text elements with translations
  const elements = document.querySelectorAll("[data-lang-key]")
  elements.forEach((element) => {
    const key = element.getAttribute("data-lang-key")
    if (translations[lang][key]) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = translations[lang][key]
      } else {
        element.textContent = translations[lang][key]
      }
    }
  })

  // Store the language preference
  localStorage.setItem("preferredLanguage", lang)
}

// Initialize language toggle
document.addEventListener("DOMContentLoaded", () => {
  // Check for stored language preference
  const storedLang = localStorage.getItem("preferredLanguage") || "en"
  setLanguage(storedLang)

  // Add event listeners to language toggle buttons
  const enToggle = document.getElementById("en-toggle")
  const esToggle = document.getElementById("es-toggle")

  if (enToggle && esToggle) {
    enToggle.addEventListener("click", () => {
      setLanguage("en")
    })

    esToggle.addEventListener("click", () => {
      setLanguage("es")
    })
  }
})
